# Script complet: Login + POST CDW
$apiBase = "http://localhost:8080"
$missionId = "451fad2e-99ba-4d5a-af10-037f1adf913d"

Write-Host "======================================"
Write-Host "STEP 1: LOGIN"
Write-Host "======================================"
Write-Host ""

# Tentez plusieurs identifiants courants
$credentials = @(
    @{ username = "admin"; password = "admin" },
    @{ username = "test"; password = "password" },
    @{ username = "user"; password = "user" },
    @{ username = "audit"; password = "audit" }
)

$token = $null
$loginUser = $null

foreach ($cred in $credentials) {
    try {
        $loginData = @{
            "username" = $cred.username
            "password" = $cred.password
        } | ConvertTo-Json

        Write-Host "Trying credentials: $($cred.username) / $($cred.password)..." -ForegroundColor Yellow

        $response = Invoke-WebRequest -Uri "$apiBase/auth/signin" `
            -Method POST `
            -ContentType "application/json" `
            -Body $loginData `
            -ErrorAction Stop

        $responseData = $response.Content | ConvertFrom-Json
        
        if ($responseData.data -and $responseData.data.token) {
            $token = $responseData.data.token
            $loginUser = $cred.username
            Write-Host "Login SUCCESS!" -ForegroundColor Green
            Write-Host "Token: $($token.Substring(0, 30))..." -ForegroundColor Green
            Write-Host ""
            break
        }
    } catch {
        # Try next credential
    }
}

if (-not $token) {
    Write-Host "Login FAILED - Could not authenticate with any credentials" -ForegroundColor Red
    Write-Host ""
    Write-Host "Make sure you have a valid user account created" -ForegroundColor Yellow
    exit 1
}

Write-Host "======================================"
Write-Host "STEP 2: CREATE CDW"
Write-Host "======================================"
Write-Host ""

