Write-Host "Configuration:" -ForegroundColor Yellow
Write-Host "  Endpoint: POST $apiBase/api/cdws/$missionId" -ForegroundColor White
Write-Host "  Mission ID: $missionId" -ForegroundColor White
Write-Host "  CD/W No.: CDW-001" -ForegroundColor White
Write-Host "  Authenticated as: $loginUser" -ForegroundColor White
Write-Host ""

Write-Host "Sending POST request..." -ForegroundColor Yellow

try {
    $headers = @{
        "Authorization" = "Bearer $token"
        "Content-Type" = "application/json"
    }

    $response = Invoke-WebRequest -Uri "$apiBase/api/cdws/$missionId" `
        -Method POST `
        -ContentType "application/json" `
        -Headers $headers `
        -Body $cdwData `
        -ErrorAction Stop
    
    Write-Host "SUCCESS - Response received" -ForegroundColor Green
    Write-Host ""
    Write-Host "Status Code: $($response.StatusCode)" -ForegroundColor Green
    Write-Host ""
    
    $responseData = $response.Content | ConvertFrom-Json
    
    Write-Host "Response Details:" -ForegroundColor Cyan
    Write-Host "  Success: $($responseData.success)" -ForegroundColor White
    Write-Host "  Message: $($responseData.message)" -ForegroundColor White
    Write-Host ""
    
    if ($responseData.data) {
        Write-Host "Created Data:" -ForegroundColor Cyan
        Write-Host "  ID: $($responseData.data.id)" -ForegroundColor White
        Write-Host "  Mission ID: $($responseData.data.missionId)" -ForegroundColor White
